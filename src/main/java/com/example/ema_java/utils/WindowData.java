package com.example.ema_java.utils;

public class WindowData {
    public String View_Name;

    public String Title;

    public double V_Size;

    public double H_Size;

    public WindowData(String view_Name, String title, double v_Size, double h_Size) {
        View_Name = view_Name;
        Title = title;
        V_Size = v_Size;
        H_Size = h_Size;
    }
}
